dia = int(input("Digite o número de 1 a 7: "))


match dia:
    case 1:
        print("Domingo")
    case 2:
        print("Segunda")
    case 3:
        print("terça feira")
    case 4:
        print("quarta feira")
    case 5:
        print("quinta feira")
    case 6:
        print("sexta feira")
    case 7:
        print("sábado")